<br><br>
<P>
This project examines preliminary versions of the GMAO Unified GEOS-5 GCM.  The GEOS-5 GCM incorporates the finite-volume
dynamical core (S.J. Lin) together with a complete physics package including radiation (Chou and Suarez), 
moist processes (Moorthi and Suarez, Bacmeister), and a catchment land surface model (Koster and Suarez).  
In addition, the GEOS-5 GCM utilizes the software design developed through the Earth System Modeling 
Framework (ESMF) Project.
</P>

